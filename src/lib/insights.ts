export function generateInsights(dataCounts: DataCounts): WellnessInsight[] {
  const insights: WellnessInsight[] = [];

  // Only surface cycle+sleep insights with sufficient data
  if (dataCounts.periodLogs >= MIN_PERIOD_LOGS && dataCounts.sleepLogs >= MIN_SLEEP_LOGS) {
    insights.push(CYCLE_SLEEP_INSIGHTS[0]);
    if (dataCounts.sleepLogs >= 14) {
      insights.push(CYCLE_SLEEP_INSIGHTS[1]);
    }
  }

  // Wellness insights need multiple data types
  if (dataCounts.periodLogs >= MIN_PERIOD_LOGS) {
    insights.push(WELLNESS_INSIGHTS[0]);
  }
  if (dataCounts.sleepLogs >= MIN_SLEEP_LOGS) {
    insights.push(WELLNESS_INSIGHTS[1]);
    insights.push(WELLNESS_INSIGHTS[2]);
  }

  // Pattern insights need symptom + mood data
  if (dataCounts.symptomLogs >= MIN_SYMPTOM_LOGS) {
    insights.push(PATTERN_INSIGHTS[0]);
  }
  if (dataCounts.moodLogs >= MIN_MOOD_LOGS) {
    insights.push(PATTERN_INSIGHTS[1]);
  }

  return insights;
}

