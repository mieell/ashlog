// Ash AI assistant mock responses
export function getAshResponse(question: string, context: any = null): string {
  const q = question.toLowerCase();

  const getDaysSince = (dateString?: string | Date) => {
    if (!dateString) return null;
    return Math.floor((new Date().getTime() - new Date(dateString).getTime()) / (1000 * 60 * 60 * 24));
  };

  const daysSincePeriod = getDaysSince(context?.lastPeriod?.date);
  
  if (q.includes("next period") || q.includes("when is my")) {
    if (daysSincePeriod !== null) {
      const estimatedCycleLength = 28;
      const daysUntilNext = estimatedCycleLength - daysSincePeriod;
      if (daysUntilNext > 0) {
        return `Based on your last logged period (${daysSincePeriod} days ago), your next period is estimated to start in about ${daysUntilNext} days. Keep in mind that slight variations are completely normal.`;
      } else if (daysUntilNext === 0) {
        return `Based on your cycle history, your next period is estimated to start today.`;
      } else {
        return `Your period is currently estimated to be ${Math.abs(daysUntilNext)} days late. Variations are common, but consider taking a test or consulting a healthcare provider if it persists.`;
      }
    }
    return "I don't have enough data about your last period. Please log your period dates so I can estimate your next cycle.";
  }

  if (q.includes("late") || q.includes("why is my cycle")) {
    if (daysSincePeriod !== null && daysSincePeriod > 28) {
      return `Your period is currently ${daysSincePeriod - 28} days later than the standard 28-day cycle. Variations are common and can be influenced by stress, sleep changes, exercise, or diet. If it is more than 2 weeks late, consider consulting your healthcare provider.`;
    }
    return "Cycle variations of up to 7 days are common and can be influenced by stress, sleep changes, exercise, or diet. If your period is more than 2 weeks late, consider consulting your healthcare provider.";
  }

  if (q.includes("symptoms") || q.includes("before my period")) {
    if (context?.lastMood?.symptoms && context.lastMood.symptoms.length > 0) {
      const parsedSymptoms = typeof context.lastMood.symptoms === 'string' 
        ? JSON.parse(context.lastMood.symptoms) 
        : context.lastMood.symptoms;
      if (Array.isArray(parsedSymptoms) && parsedSymptoms.length > 0) {
        return `Based on your recent logs, you recently experienced: ${parsedSymptoms.join(", ")}. It's helpful to track these throughout your cycle.`;
      }
    }
    return "Based on your logs, your most common pre-period symptoms are bloating, mild cramps, and mood changes. These typically appear 2-3 days before menstruation. Keep logging your symptoms for better tracking!";
  }

  if (q.includes("regular") || q.includes("how regular")) {
    return "Your cycles over the past few months have been fairly consistent, averaging around 28 days. A variation of 1-2 days is perfectly normal.";
  }

  if (q.includes("sleep") || q.includes("insomnia")) {
    if (context?.lastSleep?.hours) {
      return `I noticed you logged ${context.lastSleep.hours} hours of sleep recently with a quality rating of ${context.lastSleep.quality}/5. Your sleep quality tends to dip during the late luteal phase. Try maintaining a consistent bedtime routine!`;
    }
    return "Your sleep quality tends to dip during the late luteal phase. Try maintaining a consistent bedtime routine during this phase, and consider limiting screen time before bed.";
  }

  if (q.includes("mood") || q.includes("feeling")) {
    if (context?.lastMood?.mood) {
      return `You recently logged your mood as "${context.lastMood.mood}". Your mood patterns show natural fluctuations throughout your cycle. Many women experience lower mood during the late luteal phase, which typically improves once menstruation begins.`;
    }
    return "Your mood patterns show natural fluctuations throughout your cycle. Many women experience lower mood during the late luteal phase, which typically improves once menstruation begins.";
  }

  return "I can help you understand your cycle patterns, sleep quality, and mood trends. Try asking me about your next period, cycle regularity, sleep patterns, or common symptoms.";
}
